package com.youyicn.entity.cycle;

//首页登录显示的事件数量
public class LoginEvent {
	private String userName;
	private Integer cheR;
	private Integer caseT;
	private Integer lecT;
	private Integer prac;
	private Integer inLea;
	
	public String getName() {
		return userName;
	}
	public void setName(String userName) {
		this.userName = userName;
	}
	public Integer getCheR() {
		return cheR;
	}
	public void setCheR(Integer cheR) {
		this.cheR = cheR;
	}
	public Integer getCaseT() {
		return caseT;
	}
	public void setCaseT(Integer caseT) {
		this.caseT = caseT;
	}
	public Integer getLecT() {
		return lecT;
	}
	public void setLecT(Integer lecT) {
		this.lecT = lecT;
	}
	public Integer getPrac() {
		return prac;
	}
	public void setPrac(Integer prac) {
		this.prac = prac;
	}
	public Integer getInLea() {
		return inLea;
	}
	public void setInLea(Integer inLea) {
		this.inLea = inLea;
	}

	
}
